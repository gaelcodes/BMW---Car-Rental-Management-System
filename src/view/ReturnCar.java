   private void SearchRent() {
    String keyword = SearchTb.getText().trim();
    if (keyword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a search keyword.");
        return;
    }
    try {
        ResultSet rs = new dao.ReturnDao().searchRent(keyword);
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                    new Object[] { "RENTID", "PLATENUM", "MODEL", "CUSTNAME", "RENTDATE", "RETURNDATE", "RENTFEE" }, 0);
            while (Rs.next()) {
                model.addRow(new Object[] {
                        Rs.getString("RENTID"),
                        Rs.getString("PLATENUM"),
                        Rs.getString("MODEL"),
                        Rs.getString("CUSTNAME"),
                        Rs.getDate("RENTDATE"),
                        Rs.getDate("RETURNDATE"),
                        Rs.getString("RENTFEE")
                });
            }
            RentTable.setModel(model);
        } catch (Exception e) {
            
        }
   }

