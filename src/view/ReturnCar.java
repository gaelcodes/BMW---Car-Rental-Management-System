    private void DeleteReturnCar() {
        // Implement the method to delete a rent car from the database
        try {
            Con = DriverManager.getConnection(url_db, username_db, password_db);
            String query = "DELETE FROM returntable WHERE PLATENUM=?";
            PreparedStatement pst = Con.prepareStatement(query);
            pst.setString(1, PlateNumTb1.getText());
            int row = pst.executeUpdate();
            if (row > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Car Deleted Successfully");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error Deleting Car");
            }
            Con.close();
        } catch (Exception e) {
        }
    }

    private void UpdateCar() {

        try {
            Con = DriverManager.getConnection(url_db, username_db, password_db);
            String Plate = PlateNumTb1.getText();
            String CarStatus = "Available";
            // Update the car status in the database
            String query = "UPDATE carstable SET STATUS=? WHERE PLATENUM=?";
            // Use PreparedStatement to prevent SQL injection
            PreparedStatement pst = Con.prepareStatement(query);
            pst.setString(1, CarStatus);
            pst.setString(2, Plate);
            int row = pst.executeUpdate();
            if (row > 0) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Car Has Been Returned Successfully, And Now Available For Rent");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error While Updating Car");
            }
            Con.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    

